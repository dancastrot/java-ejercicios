package ejercicios;

public class Practica {

	public static void main(String[] args) {
		
		byte numeroByte = 127;
		int numeroInt = 200;
		double numeroDouble = 500.2;
		float numeroFloat = 524.65F;
		long numeroLong = 2144544454531L;
		
		char letra = 'A';
		String palabra = "OpenBootcamp";
		
		boolean gratis = true;
		
		System.out.println("Byte: " + numeroByte);
		System.out.println("Int: " + numeroInt);
		System.out.println("Double: " + numeroDouble);
		System.out.println("Float: " + numeroFloat);
		System.out.println("Long: " + numeroLong);
		System.out.println("Char: " + letra);
		System.out.println("String: " + palabra);
		System.out.println("Boolean: " + gratis);
		
	}

}
